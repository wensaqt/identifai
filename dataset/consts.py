"""Dataset-level enums and constants.

DocType and FieldName mirror backend/consts/ values so the dataset package
remains self-contained (no runtime dependency on backend/).
"""
from enum import StrEnum


class AnomalyType(StrEnum):
    """All anomaly types detectable by the verifier."""
    SIRET_MISMATCH = "siret_mismatch"
    EXPIRED_ATTESTATION = "expired_attestation"
    TVA_MISMATCH = "tva_mismatch"
    PAYMENT_AMOUNT_MISMATCH = "payment_amount_mismatch"
    ORPHAN_PAYMENT = "orphan_payment"
    MISSING_PAYMENT = "missing_payment"
    UNDECLARED_REVENUE = "undeclared_revenue"


class DocType(StrEnum):
    """Document types generated by the dataset factories."""
    INVOICE = "invoice"
    DEVIS = "devis"
    ATTESTATION_SIRET = "attestation_siret"
    ATTESTATION_URSSAF = "attestation_urssaf"
    KBIS = "kbis"
    RIB = "rib"
    PAYMENT = "payment"
    URSSAF_DECLARATION = "urssaf_declaration"


class FieldName(StrEnum):
    """Field names used in document records and ground-truth metadata."""
    SIRET = "siret"
    SIRET_EMETTEUR = "siret_emetteur"
    SIREN = "siren"
    IBAN = "iban"
    MONTANT_HT = "montant_ht"
    MONTANT_TTC = "montant_ttc"
    MONTANT_TVA = "montant_tva"
    MONTANT = "montant"
    TVA_RATE = "tva_rate"
    CHIFFRE_AFFAIRES_DECLARE = "chiffre_affaires_declare"
    DATE_EMISSION = "date_emission"
    DATE_EXPIRATION = "date_expiration"
    DATE_PAIEMENT = "date_paiement"
    DATE_DECLARATION = "date_declaration"
    PERIODE = "periode"
    INVOICE_ID = "invoice_id"
    PAYMENT_ID = "payment_id"
    REFERENCE_FACTURE = "reference_facture"
    STATUT_PAIEMENT = "statut_paiement"
    MONTANT_PAIEMENT = "montant_paiement"  # summary alias for payment.montant
